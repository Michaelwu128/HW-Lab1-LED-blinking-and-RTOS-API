/* USER CODE BEGIN 4 */
uint32_t button_press_time = 0;   // 記錄按下的時間點

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if (GPIO_Pin == BUTTON_EXTI13_Pin) {

    if (HAL_GPIO_ReadPin(GPIOC, BUTTON_EXTI13_Pin) == GPIO_PIN_RESET) {
      // 偵測到按下去，記錄時間
      button_press_time = HAL_GetTick();
    }
    else {
      // 偵測到放開 (變成 SET)
      // 防護機制：確保真的有按下去的紀錄，才去計算時間差
      if (button_press_time != 0) {
          uint32_t duration = HAL_GetTick() - button_press_time;

          uint8_t msg = 0;
          if (duration > 1000) {
            msg = 2; // 時間差 > 1秒，長按
          } else if (duration > 50) {
            msg = 1; // 時間差 > 50ms，短按 (兼具防彈跳)
          }

          // 把訊息丟進 Queue 給 Task_1
          if (msg != 0) {
            osMessageQueuePut(Queue_ButtonHandle, &msg, 0, 0);
          }

          // 計算完畢後清空，等待下一次按鍵
          button_press_time = 0;
      }
    }
  }
}


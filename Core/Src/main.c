/**
* @brief Function implementing the Task_1 thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTask_1 */
void StartTask_1(void *argument)
{
  /* USER CODE BEGIN StartTask_1 */
  uint8_t received_msg;

  for(;;)
  {
    // 1. 等待 Queue 的訊息 (osWaitForever 代表一直等下去)
    if (osMessageQueueGet(Queue_ButtonHandle, &received_msg, NULL, osWaitForever) == osOK) {

      // 2. 取得 Mutex 保護 LED，避免 Task_2 跑來亂
      osMutexAcquire(Mutex_LEDHandle, osWaitForever);

      if (received_msg == 1) {
        // 短按：閃 5 秒，頻率 1Hz (亮 500ms, 暗 500ms，共 5 次)
        for (int i = 0; i < 5; i++) {
          HAL_GPIO_WritePin(GPIOB, LED2_Pin, GPIO_PIN_SET);
          osDelay(500);
          HAL_GPIO_WritePin(GPIOB, LED2_Pin, GPIO_PIN_RESET);
          osDelay(500);
        }
      }
      else if (received_msg == 2) {
        // 長按：閃 5 秒，頻率 10Hz (亮 50ms, 暗 50ms，共 50 次)
        for (int i = 0; i < 50; i++) {
          HAL_GPIO_WritePin(GPIOB, LED2_Pin, GPIO_PIN_SET);
          osDelay(50);
          HAL_GPIO_WritePin(GPIOB, LED2_Pin, GPIO_PIN_RESET);
          osDelay(50);
        }
      }

      // 3. 閃爍完畢，釋放 Mutex
      osMutexRelease(Mutex_LEDHandle);
    }
  }
  /* USER CODE END StartTask_1 */
}

